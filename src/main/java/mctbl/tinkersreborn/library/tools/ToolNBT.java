package mctbl.tinkersreborn.library.tools;

import java.util.List;

import net.minecraft.nbt.NBTTagCompound;

import mctbl.tinkersreborn.TinkersRebornConfig;
import mctbl.tinkersreborn.tools.materials.ExtraMaterialStats;
import mctbl.tinkersreborn.tools.materials.HandleMaterialStats;
import mctbl.tinkersreborn.tools.materials.HeadMaterialStats;
import mctbl.tinkersreborn.util.ToolTags;

public class ToolNBT {

    public int durability;
    public int harvestLevel;
    public float attack;
    public float speed; // mining speed
    public float attackSpeedMultiplier;
    public int modifiers; // free modifiers

    private final NBTTagCompound parent;

    public ToolNBT() {
        durability = 0;
        harvestLevel = 0;
        attack = 0;
        speed = 0;
        attackSpeedMultiplier = 1;
        modifiers = TinkersRebornConfig.defaultModifiers;
        parent = new NBTTagCompound();
    }

    public ToolNBT(NBTTagCompound tag) {
        read(tag);
        parent = tag;
    }

    /** Initialize the stats with the heads. CALL THIS FIRST */
    public ToolNBT head(HeadMaterialStats... heads) {
        this.durability = 0;
        this.harvestLevel = 0;
        this.attack = 0;
        this.speed = 0;

        // average all stats
        for (HeadMaterialStats head : heads) {
            if (head != null) {
                durability += head.durability;
                attack += head.attack;
                speed += head.miningspeed;

                // use highest harvestlevel
                this.harvestLevel = Math.max(head.harvestLevel, this.harvestLevel);
            }
        }

        durability = Math.max(1, durability / heads.length);
        attack /= (float) heads.length;
        speed /= (float) heads.length;

        return this;
    }

    public ToolNBT head(List<HeadMaterialStats> heads) {
        return this.head(heads.toArray(new HeadMaterialStats[0]));
    }

    /** Add stats from the accessoires. Call this second! */
    public ToolNBT extra(ExtraMaterialStats... extras) {
        int dur = 0;
        for (ExtraMaterialStats extra : extras) {
            if (extra != null) {
                dur += extra.extraDurability;
            }
        }
        this.durability += Math.round((float) dur / (float) extras.length);

        return this;
    }

    public ToolNBT extra(List<ExtraMaterialStats> extras) {
        return this.extra(extras.toArray(new ExtraMaterialStats[0]));
    }

    /** Calculate in handles. call this last! */
    public ToolNBT handle(HandleMaterialStats... handles) {
        // (Average Head Durability + Average Extra Durability) * Average Handle
        // Modifier + Average Handle Durability

        int dur = 0;
        float modifier = 0f;
        for (HandleMaterialStats handle : handles) {
            if (handle != null) {
                dur += handle.durability;
                modifier += handle.multiplier;
            }
        }

        modifier /= (float) handles.length;
        this.durability = Math.round((float) this.durability * modifier);

        // add in handle durability change
        this.durability += Math.round((float) dur / (float) handles.length);

        this.durability = Math.max(1, this.durability);

        return this;
    }

    public ToolNBT handle(List<HandleMaterialStats> handles) {
        return this.handle(handles.toArray(new HandleMaterialStats[0]));
    }

    public void read(NBTTagCompound tag) {
        durability = tag.getInteger(ToolTags.DURABILITY);
        harvestLevel = tag.getInteger(ToolTags.HARVESTLEVEL);
        attack = tag.getFloat(ToolTags.ATTACK);
        speed = tag.getFloat(ToolTags.MININGSPEED);
        attackSpeedMultiplier = tag.getFloat(ToolTags.ATTACKSPEEDMULTIPLIER);
        modifiers = tag.getInteger(ToolTags.FREE_MODIFIERS);
    }

    public void write(NBTTagCompound tag) {
        tag.setInteger(ToolTags.DURABILITY, durability);
        tag.setInteger(ToolTags.HARVESTLEVEL, harvestLevel);
        tag.setFloat(ToolTags.ATTACK, attack);
        tag.setFloat(ToolTags.MININGSPEED, speed);
        tag.setFloat(ToolTags.ATTACKSPEEDMULTIPLIER, attackSpeedMultiplier);
        tag.setInteger(ToolTags.FREE_MODIFIERS, modifiers);
    }

    public NBTTagCompound get() {
        NBTTagCompound tag = (NBTTagCompound) parent.copy();
        write(tag);
        return tag;
    }

    // Autogenerated equals and hashcode
    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        ToolNBT toolNBT = (ToolNBT) o;

        if (durability != toolNBT.durability) {
            return false;
        }
        if (harvestLevel != toolNBT.harvestLevel) {
            return false;
        }
        if (Float.compare(toolNBT.attack, attack) != 0) {
            return false;
        }
        if (Float.compare(toolNBT.speed, speed) != 0) {
            return false;
        }
        return modifiers == toolNBT.modifiers;

    }

    @Override
    public int hashCode() {
        int result = durability;
        result = 31 * result + harvestLevel;
        result = 31 * result + (attack != +0.0f ? Float.floatToIntBits(attack) : 0);
        result = 31 * result + (speed != +0.0f ? Float.floatToIntBits(speed) : 0);
        result = 31 * result + modifiers;
        return result;
    }
}
